liczby_calkowite = ('1', '2','3','4','5','6','7')

# drugi element
for i in liczby_calkowite[2]:
    print(i)

# przedostatni element
for i in liczby_calkowite[-2]:
    print(i)
# od trzeciego do 4 (włącznie)
for i in liczby_calkowite[3:5]:
    print(i)
# co 2 liczac od konca
for i in liczby_calkowite[-1:0]:
    print(i)